    <h2 id="introduction" style="display: table; border: 6px solid rgb(250,100,20); background: rgb(250,100,20); border-radius: 7px; color: white;">
        Introduction
    </h2>

    <a>
        Caramel est un logiciel de gestion de la relation client. Appelé plus
        généralement par son acronyme CRM venant de l'anglais "Customer
        Relationship Managent", le CRM a pour but de faciliter la gestion de
        nombreuses données relatives à des individus, groupes d'individus ou
        encore des évènements. Dans notre cas la gestion de donateurs, de leurs
        dons, des clients des associations ou la gestion de campagnes.
    </a>

    <h2 id="demarrage" style="display: table; border: 6px solid rgb(250,100,20); background: rgb(250,100,20); border-radius: 7px; color: white;">
        Démarrage
    </h2>
    <a>
        Lors du démarrage du logiciel il est demandé à l'utilisateur de rentrer
        ses informations d'authentification afin de se connecter à Caramel. Le
        premier champ permet de renseigner votre nom d'utilisateur et le deuxième
        le mot de passe qui lui est associé. Pour tout problème lié à
        l'authentification, veuillez contacter l'administrateur du logiciel.
    </a>

