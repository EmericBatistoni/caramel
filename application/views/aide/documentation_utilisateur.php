                Manuel d'aide
            </h4>
            <a>
                Le manuel d'aide permet d'ouvrir cette fenêtre. Lorsque vous
                cliquez sur l'icône, le logiciel analyse ce qui est actuellement
                présent sur votre écran et vous affiche la partie du manuel la
                plus susceptible de vous aider. L'ensemble de l'aide est
                cependant toujours accessible, pour cela il suffit d'utiliser
                la molette de votre souris.
                <br>
                <br><B>A noter :</B>
                En laissant votre souris sur un élément dans caramel vous ferez
                apparaître une boîte de dialogue expliquant sa fonction.
