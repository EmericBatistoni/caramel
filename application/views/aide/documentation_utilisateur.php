        <h3 id="barre_navigation" style="display: table; border: 6px solid rgb(250,120,40); background: rgb(250,120,40); border-radius: 7px; color: white;">
            1. La barre de navigation
        </h3>
	    <a>
            La navigation dans le logiciel Caramel est très simple. En effet,
            elle s'articule principalement autour d'une unique barre de
            navigation placée en haut du logiciel. Celle-ci permet d'accéder aux
            différentes fonctionnalités classées par rubriques. A gauche les
            fonctionnalités focalisées sur l'utilisation du logiciel et à droite
            les foncitonnalités liées aux paramétrages.
        </a>

        <h3 id="fonctionnalites_principales" style="display: table; border: 6px solid rgb(250,120,40); background: rgb(250,120,40); border-radius: 7px; color: white;">
            2. Fonctionnalités principales
        </h3>

            <h4 id="accueil" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
            Accueil
            </h4>
            <a>
                Représentée par une maison, cette fonctionnalité permet de
                retourner sur la page d'accueil du logiciel. L'accueil met à
                votre disposition la barre de navigation dont nous avons parlé
                précédemment. C'est la page sur laquelle l'utilisateur se trouve
                après avoir réussi à s'authentifier.
                <br>
                <B><FONT color="red">Attention :</FONT></B> Appuyer sur le
                bouton d'accueil annulera toute action commencée et non
                terminée/sauvegardée.
            </a>

            <h4 id="contact" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
                Contacts
            </h4>
	        <a>
                La rubrique "contacts" est focalisée sur les individus et leurs
                informations enregistrées dans la base de données du logiciel.
                Depuis cette rubrique il est possible de :
            </a>
            <br>
            <a><br>
                <U>Rechercher un contact</U>
                <br>
                    Depuis la page d'accueil, cliquez sur l'onglet "Contacts" dans
                    la barre de navigation. Vous pouvez alors effectuer une recherche
                    rapide ou avancée.
                <br>
                <br>Recherche rapide :<br>
                Pour rechercher rapidement un individu, entrez son nom dans le
                champ vide prévu à cet effet.Il est possible de renseigner soit
                le nom, soit le prénom ou juste le début d'un d'entre eux. La
                liste des contacts correspondant à votre recherche apparaît.
                <br>
                <br>Recherche avancée :<br>
                Pour rechercher un contact en fonction d'un critère spécifique,
                il possible d'effectuer une recherche avancée disponible en
                appuyant sur l'icône suivant "recherche avancée".Un formulaire
                de contact vide apparaît. Il suffit de remplir un des différents
                champs et d'appuyer sur le bouton "rechercher" en bas à droite
                pour afficher les contacts répondants au(x) critère(s) de la
                recherche.
                <br>
                <br><B>A noter :</B> Cliquer sur le bouton "recherche rapide" en
                laissant la case vide pour afficher toute la liste.
            </a><br>
	        <a><br>
                <U>Ajouter un contact</U>
                <br>
                    Depuis la page d'accueil, cliquez sur l'onglet "Contacts"
                    dans la barre de navigation. Vous pouvez alors créer un
                    nouveau contact en appuyant sur le bouton bleu "créer un
                    nouveau contact". Un formulaire vide est généré, il permet
                    de renseigner les informations essentielles sur le nouveau
                    contact. Une fois le formulaire rempli, cliquez sur
                    "sauvegarder" en bas à droite.
            </a><br>
	        <a><br>
                <U>Editer un contact</U>
                <br>
                    Pour éditer un contact vous devez d'abord le rechercher dans
                    le logiciel. Pour cela référez vous à la rubrique "rechercher
                    un contact". Une fois votre contact affiché à l'écran,
                    cliquez sur l'icône d'édition tout à gauche représenté par
                    une feuille et un crayon.
            </a><br>
	        <a><br>
                <U>Supprimer un contact</U>
                <br>
                    Afin de supprimer un contact, recherchez-le comme vu
                    précédemment puis appuyez sur la croix à droite du logo d'édition.
            </a>

            <h4 id="don" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
                Versements
            </h4>
	        <a>
                L'onglet "versements" permet de consulter les versements déjà
                effectués et d'en créer de nouveaux. Ils sont définis par un
                code unique, l'identifiant du contact associé, un montant, un
                type de versement (ex: cotisation, don, etc.), une date de
                saisie et un reçu fiscal. Cet onglet donne accès aux
                fonctionnalités suivantes :
            </a><br>
	        <a><br>
                <U>Rechercher un versement</U>
                <br>
                    Depuis la page d'accueil, cliquez sur l'onglet "Versements"
                    dans la barre de navigation. Vous pouvez alors effectuer une
                    recherche rapide ou avancée.
                <br>
                <br>Recherche rapide :
                <br>Pour rechercher rapidement une campagne, entrez son code
                dans le champ vide prévu à cet effet .Il est possible de
                renseigner le numéro d’adhérent de la personnes ayant fait le
                versement. La liste des versemtenss correspondant à votre
                recherche apparaît.<br><br>Recherche avancée :
                <br>Pour rechercher un versement en fonction d'un critère
                spécifique, il possible d'effectuer une recherche avancée
                disponible en appuyant sur l'icône suivant "recherche avancée".
                Un formulaire vide apparaît. Il suffit de remplir un des
                différents champ et d'appuyer sur le bouton "rechercher" en bas
                à droite pour afficher les versements répondant au(x) critère(s)
                de la recherche.
                <br>
                <br>
                <B>A noter :</B> Cliquer sur le bouton "recherche rapide" en
                laissant la case vide pour afficher toute la liste.
            </a><br>
	        <a><br>
                <U>Créer un nouveau versement</U>
                <br>Depuis la page d'accueil, cliquez sur l'onglet "Versements"
                dans la barre de navigation. Vous pouvez alors créer un nouveau
                versement en appuyant sur le bouton bleu "créer un nouveau
                versement". Un formulaire vide est généré, il permet de
                renseigner les informations essentielles concernant le versement.
                Une fois le formulaire rempli, cliquez sur "sauvegarder" en bas
                à droite.
            </a><br>
	        <a><br>
                <U>Supprimer ou éditer un versement</U>
                <br>Pour éditer un versement vous devez d'abord le rechercher
                dans le logiciel. Pour cela référez vous à la rubrique
                "rechercher un versement". Une fois votre (vos) versement(s)
                affiché(s) à l'écran, cliquez sur l'icône d'édition tout à
                gauche représenté par un feuille et un crayon.
                <br>La suppression du versement correspond à l’icône en forme de
                croix rouge.
            </a><br>
	        <a><br>
                <U>Editer un reçu fiscal ou voir le supplicata</U>
                <br>Pour ce faire, vous devez d'abord le rechercher dans le
                logiciel. Pour cela référez vous à la rubrique "rechercher un
                versement". Une fois votre (vos) versement(s) affiché(s) à
                l'écran, cliquez sur le bouton sur la droite de la ligne afin
                d’éditer le reçu ou voir le duplicata si l’édition a déjà été faite
            </a><br>

            <h4 id="campagne" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
                Campagnes
            </h4>
            <a>
                L'onglet "campagnes" permet de visualiser les campagnes déjà
                effectuées ou en cours. Les campagnes sont des évènements
                définis par un nom (ex: Don du Sang), un code (ex: DonSang), une
                date de début et de fin ainsi qu'un type de campagne
                (ex: prospection, fidélisation, etc..). Cet onglet permet de :
            </a><br>
            <a><br>
                <U>Rechercher une campagne</U>
            </a><br>
            <a>
                Pour rechercher une campagne cliquez sur l'onglet campagne dans
                la barre de navigation de la page d'accueil. Vous pouvez
                rechercher une campagne avec la fonction rapide ou avancée.
                <br/>
                <br/>Recherche rapide :<br/>
                Chaque campagne possède un nom et un code. Inscrivez l'un ou
                l'autre dans l'encadré prévu à cette effet puis cliquez sur
                "recherche rapide" pour afficher les résultats.
                <br>
                <br><B>A noter :</B> Il est essentiel de rentrer le nom complet
                d'un code pour que la recherche aboutisse.
                <br>
                <br>Recherche avancée :<br>
                Pour effectuer une recherche avancée cliquez sur l'icône
                "recherche avancée".Un formulaire vierge apparaît, renseignez un
                ou plusieurs critères et cliquez sur "rechercher" en bas à
                droite. La liste des campagnes en fonction de vos critères
                apparaît.
                <br>
                <br><B>A noter : </B>Cliquer sur le bouton "recherche rapide" en
                laissant la case vide pour afficher toute la liste.
            </a><br/>
            <a><br>
                <U>Créer une nouvelle campagne</U>
            </a><br>
            Pour créer une nouvelle campagne, cliquez sur l'onglet campagne dans
            la barre de navigation puis sur le bouton "Créer une nouvelle
            campagne".Un formulaire vierge apparaît, remplissez le et cliquer
            sur le bouton "sauvegarder" en bas à droite.
            <br>
            <a><br>
                <U>Editer ou supprimer un campagne</U>
            </a><br>
            Afin d'éditer ou supprimer une campagne, cliquez sur l'onglet
            "campagnes" dans la barre de navigation puis recherchez une
            campagne comme vu précédemment.
            <br>
            <br>Editer :
            <br>
            Cliquer sur le crayon à côté du non de la campagne, un formulaire
            avec les informations apparaît, vous pouvez alors les modifier et
            appuyer sur "sauvegarder".
            <br>
            <br>Supprimer :
            <br>Pour supprimer une campagne, appuyer sur la croix rouge à côté
            de son nom après l'avoir recherché.

            <h4 id="offre" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
                Offres
            </h4>
            <a>
                L'onglet "offres" permet d'accéder aux offres mises en place.
                Les offres sont définies par un code (ex: OFF1), un titre
                (ex: Relance téléphonique) et une date de fin. Cet onglet offre
                les possibilités suivantes :
            </a><br>
            <a><br>
                <U>Rechercher une offre existante</U>
            </a><br>
            <br>Recherche rapide :<br>
            Il est possible d’effectuer une recherche rapide, en renseignant
            soit le code soit le libellé d’une offre. Pour cela sélectionnez
            dans le menu déroulant entre code et libellé et entrez votre
            recherche dans le champ à gauche de recherche rapide.
            <br>
            <br>Code : Le code doit être rentré dans sa totalité et sans fautes
            d’orthographe, il correspond à une abréviation/immatriculation
            permettant d’identifier rapidement une offre.
            <br>
            <br>Libellé : Le libellé permet de définir une offre, afin que ça
            fonction soit claire pour tous les utilisateurs.
            <br>
            <br>Recherche avancée :<br>
            Pour effectuer une recherche avancée cliquez sur l'icône "recherche
            avancée".Un formulaire vierge apparaît, renseignez un ou plusieurs
            critères et cliquez sur "rechercher" en bas à droite. La liste des
            offres en fonction de vos critères apparaît.
            <br>
            <a><br>
                <U>Enregistrer une nouvelle offre</U>
            </a><br>
            Afin d’enregistrer une nouvelle offre, il faut appuyer sur le bouton
            « Créer une nouvelle offre », un formulaire vierge apparait. Il est
            alors possible de donner à une offre un nom, une description, des
            dates de début et fin, etc. Apres avoir renseigné les champs désirés
            (les champs marqués d’une étoile sont obligatoires), cliquez sur le
            bouton « sauvegarder » en bas à droite.
            <br>
            <br><B>A noter :</B> une fois l’offre enregistrée, il sera
            impossible de modifier le code.
            <br>
            <a><br>
                <U>Editer ou supprimer une offre</U>
            </a><br>
            Comme pour d’autres rubriques il est possible d’éditer ou supprimer
            une ou plusieurs offres. Pour cela il faut d’abord afficher les
            offres qu’on souhaite supprimer ou éditer (voir la rubrique «
            rechercher une offre »). La croix rouge permet de supprimer une
            offre. Le crayon permet d’éditer une offre. Lorsque vous cliquez sur
            le bouton d’édition, un formulaire avec les données correspondantes
            à l’offre apparait. Il est possible de modifier toutes les données
            sauf le code de l’offre. Une fois l’édition terminée, cliquez sur
            « sauvegarder » pour valider vos modifications.

            <h4 id="segment" style="display: table; border: 6px solid rgb(250,140,70); background: rgb(250,140,70); border-radius: 7px; color: white;">
                Segments
            </h4>
            <a>
                L'onglet "segments" permet d'accéder à une liste de segments.
                Un segment permet de lier des contacts entre eux en fonction
                d'une ou plusieurs caractéristiques telles que leur âge, leur
                allergie, leur adresse, etc. Un segment est représenté par un
                code (ex: Parisiens) ainsi qu'un libellé (ex: Habitants de paris)
                . Cet onglet permet de :
            </a><br>
            <a><br>
                <U>Rechercher un segment</U>
                <br>
                Depuis la page d'accueil, cliquez sur l'onglet "Segments" dans la
                barre de navigation. Vous pouvez alors effectuer une recherche
                rapide ou avancée.
                <br>
                <br>Recherche rapide :<br>
                Pour rechercher rapidement un segment, entrez son code dans le champ
                vide prévu à cet effet .Il est possible de renseigner son libéllé.
                La liste des segments correspondant à votre recherche apparaît.
                <br>
                <br>Recherche avancée :<br>
                Pour rechercher un segment en fonction d'un critère spécifique, il
                possible d'effectuer une recherche avancée disponible en appuyant
                sur l'icône suivant "recherche avancée" .Un formulaire vide apparaît.
                Il suffit de remplir un des différents champ et d'appuyer sur le
                bouton "rechercher" en bas à droite pour afficher les segments
                répondant au(x) critère(s) de la recherche.
                <br>
                <br><B>A noter :</B> Cliquer sur le bouton "recherche rapide" en
                laissant la case vide pour afficher toute la liste.
            </a><br>
	        <a><br>
                <U>Créer un nouveau segment</U>
                <br>
                Depuis la page d'accueil, cliquez sur l'onglet "Segments" dans la
                barre de navigation. Vous pouvez alors créer un nouveau segment
                en appuyant sur le bouton bleu "créer un nouveau segment". Un
                formulaire vide est généré, il permet de renseigner les informations
                essentielles concernant le segment. Une fois le formulaire
                rempli, cliquez sur "valider" en bas à gauche sous les champs à
                remplir.
            </a><br>
            <a><br>
                <br>Pour éditer un segment vous devez d'abord le rechercher dans
                le logiciel. Pour cela référez vous à la rubrique "rechercher un
                segment". Une fois votre (vos) segment(s) affiché(s) à l'écran,
                cliquez sur l'icône d'édition tout à gauche représenté par un
                feuille et un crayon.<br>La suppression du segment correspond à
                l’icône en forme de croix rouge.
            </a><br>


